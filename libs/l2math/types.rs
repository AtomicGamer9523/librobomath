type __Float64 = f64;
type __Float32 = f32;
type __Int = i32;

/// Radians (64bit)
pub type Radian64 = __Float64;

/// Radians (32bit)
pub type Radian32 = __Float32;

/// Degrees (64bit)
pub type Degree64 = __Float64;

/// Degrees (32bit)
pub type Degree32 = __Float32;

/// A floating point number (64bit)
pub type Float64 = __Float64;

/// A floating point number (32bit)
pub type Float32 = __Float32;

/// A signed integer (32bit)
pub type Int = __Int;
